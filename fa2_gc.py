def show_options():
    """Print available filter values."""
    print("\n── Available Filter Values ─────────────────────────────────")
    print(f"  Day of Week   : {sorted(df['Day_of_Week'].unique())}")
    print(f"  Time of Day   : {sorted(df['Time_of_Day'].unique())}")
    print(f"  Location Type : {sorted(df['Location_Type'].unique())}")
    print("────────────────────────────────────────────────────────────\n")

def interactive_planner():
    """Filter ATM data interactively and display cluster + anomaly insights."""

    print("\n🏧  Welcome to the FinTrust ATM Interactive Demand Planner  🏧")
    print("Filter by Day, Time, and Location to explore demand insights.")
    print("Type 'exit' at any prompt to quit.\n")

    while True:
        show_options()
        day  = input("  Filter by Day of Week   (Enter to skip): ").strip().capitalize()
        time = input("  Filter by Time of Day   (Enter to skip): ").strip().capitalize()
        loc  = input("  Filter by Location Type (Enter to skip): ").strip().capitalize()

        if "Exit" in [day, time, loc]:
            print("\n👋 Exiting the planner. Goodbye!")
            break

        # Apply filters
        filtered = df.copy()
        if day:
            if day in df["Day_of_Week"].values:
                filtered = filtered[filtered["Day_of_Week"] == day]
            else:
                print(f"  ⚠️  '{day}' not recognized — skipping Day filter.")
        if time:
            if time in df["Time_of_Day"].values:
                filtered = filtered[filtered["Time_of_Day"] == time]
            else:
                print(f"  ⚠️  '{time}' not recognized — skipping Time filter.")
        if loc:
            if loc in df["Location_Type"].values:
                filtered = filtered[filtered["Location_Type"] == loc]
            else:
                print(f"  ⚠️  '{loc}' not recognized — skipping Location filter.")

        if filtered.empty:
            print("  ❌ No data matches these filters. Try different values.\n")
            continue

        print(f"\n✅ {len(filtered)} records matched.\n")

        # Summary statistics
        print("── Summary Statistics ─────────────────────────────────────────")
        print(filtered[["Total_Withdrawals","Total_Deposits","Cash_Demand_Next_Day"]].describe().round(0))

        # Cluster breakdown
        print("\n── Cluster Distribution ───────────────────────────────────────")
        print(filtered["Cluster_Label"].value_counts().to_string())

        # Anomaly count
        n_anom = filtered["Anomaly_IF"].sum()
        print(f"\n── Anomalies Detected : {n_anom} ({n_anom/len(filtered)*100:.1f}% of filtered data)")

        # Plot dashboard
        filter_str = f"Day={day or 'All'} | Time={time or 'All'} | Loc={loc or 'All'}"
        fig, axes = plt.subplots(1, 3, figsize=(16, 5))
        fig.suptitle(f"Interactive Planner – {filter_str}", fontsize=12, fontweight="bold")

        # Histogram
        axes[0].hist(filtered["Total_Withdrawals"], bins=20, color="#4C72B0", edgecolor="white")
        axes[0].set_title("Withdrawal Distribution")
        axes[0].set_xlabel("Withdrawals"); axes[0].set_ylabel("Frequency")

        # Cluster bar chart
        filtered["Cluster_Label"].value_counts().plot(kind="bar", ax=axes[1],
                                                       color="steelblue", edgecolor="white")
        axes[1].set_title("Cluster Distribution")
        axes[1].set_xlabel("Cluster"); axes[1].set_ylabel("Count")
        axes[1].tick_params(axis="x", rotation=30)

        # Anomaly scatter
        fn = filtered[filtered["Anomaly_IF"] == False]
        fa = filtered[filtered["Anomaly_IF"] == True]
        axes[2].scatter(fn["Previous_Day_Cash_Level"], fn["Total_Withdrawals"],
                        alpha=0.4, s=15, color="#4C72B0", label="Normal")
        axes[2].scatter(fa["Previous_Day_Cash_Level"], fa["Total_Withdrawals"],
                        s=40, color="#DD8452", label="Anomaly", zorder=5)
        axes[2].set_title("Anomalies in Filtered Data")
        axes[2].set_xlabel("Prev Day Cash Level"); axes[2].set_ylabel("Total Withdrawals")
        axes[2].legend()

        plt.tight_layout()
        plt.savefig("planner_output.png", dpi=100); plt.show()

        again = input("\n🔄 Run another filter? (yes / no): ").strip().lower()
        if again != "yes":
            print("\n👋 Thank you for using the ATM Demand Planner!")
            break


# Run the interactive planner
