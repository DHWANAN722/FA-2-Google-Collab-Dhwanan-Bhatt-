print("📌 Observation: Holiday days show a shifted distribution toward higher withdrawals, confirming elevated demand.")

# ── 5.2 Z-Score Anomaly Detection ───────────────────────────────────────
df["Z_Score"] = np.abs(stats.zscore(df["Total_Withdrawals"]))
df["Anomaly_ZScore"] = df["Z_Score"] > 2.5
print(f"✅ Z-Score Anomalies detected: {df['Anomaly_ZScore'].sum()}")

# ── 5.3 IQR Anomaly Detection ─────────────────────────────────────────────
Q1 = df["Total_Withdrawals"].quantile(0.25)
Q3 = df["Total_Withdrawals"].quantile(0.75)
IQR_val = Q3 - Q1
df["Anomaly_IQR"] = (df["Total_Withdrawals"] < Q1 - 1.5*IQR_val) | (df["Total_Withdrawals"] > Q3 + 1.5*IQR_val)
print(f"✅ IQR Anomalies detected: {df['Anomaly_IQR'].sum()}")

# ── 5.4 Isolation Forest ─────────────────────────────────────────────────
iso = IsolationForest(contamination=0.05, random_state=42)
df["Anomaly_IF"] = iso.fit_predict(df[["Total_Withdrawals"]]) == -1
print(f"✅ Isolation Forest Anomalies detected: {df['Anomaly_IF'].sum()}")

# ── 5.5 Visualize Anomalies Over Time ─────────────────────────────────
df_sorted = df.sort_values("Date")
normal_pts  = df_sorted[df_sorted["Anomaly_IF"] == False]
anomaly_pts = df_sorted[df_sorted["Anomaly_IF"] == True]

plt.figure(figsize=(14, 5))
plt.scatter(normal_pts["Date"],  normal_pts["Total_Withdrawals"],
            alpha=0.3, s=10, color="#4C72B0", label="Normal")
plt.scatter(anomaly_pts["Date"], anomaly_pts["Total_Withdrawals"],
            s=40, color="#DD8452", label="Anomaly (Isolation Forest)", zorder=5)
plt.title("5.5 Anomaly Detection – Withdrawals Over Time", fontsize=14, fontweight="bold")
