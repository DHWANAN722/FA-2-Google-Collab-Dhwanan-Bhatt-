# STAGE 4 — CLUSTERING ANALYSIS
# =============================================================================
print("\n" + "="*60)
print("STAGE 4: Clustering Analysis of ATMs")
print("="*60)

# Encode categorical Location_Type for clustering
le = LabelEncoder()
df["Location_Type_Enc"] = le.fit_transform(df["Location_Type"])

# Select and standardize features
cluster_features = ["Total_Withdrawals", "Total_Deposits", "Location_Type_Enc", "Nearby_Competitor_ATMs"]
X = df[cluster_features].copy()
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# ── Elbow Method & Silhouette Scores ────────────────────────────────────
K_range = range(2, 11)
inertia, sil_scores = [], []
for k in K_range:
    km = KMeans(n_clusters=k, random_state=42, n_init=10)
    labels = km.fit_predict(X_scaled)
    inertia.append(km.inertia_)
    sil_scores.append(silhouette_score(X_scaled, labels))

plt.figure(figsize=(8, 5))
plt.plot(K_range, inertia, marker="o", color="#4C72B0")
plt.title("4.1 Elbow Method – Optimal Number of Clusters", fontsize=14, fontweight="bold")
plt.xlabel("Number of Clusters (K)"); plt.ylabel("Inertia")
plt.tight_layout(); plt.savefig("4_1_elbow.png", dpi=100); plt.show()
print("📌 Observation: The elbow identifies where adding more clusters gives diminishing improvement.")

best_k = list(K_range)[np.argmax(sil_scores)]
print(f"✅ Best K by Silhouette Score: {best_k} (score: {max(sil_scores):.3f})")

plt.figure(figsize=(8, 5))
plt.plot(K_range, sil_scores, marker="s", color="#DD8452")
plt.title("4.2 Silhouette Scores for Each K", fontsize=14, fontweight="bold")
plt.xlabel("Number of Clusters (K)"); plt.ylabel("Silhouette Score")
plt.tight_layout(); plt.savefig("4_2_silhouette.png", dpi=100); plt.show()


# ── Apply K-Means with Best K ────────────────────────────────────────────
kmeans = KMeans(n_clusters=best_k, random_state=42, n_init=10)
df["Cluster"] = kmeans.fit_predict(X_scaled)

# Label clusters based on average withdrawal (low → high)
cluster_means = df.groupby("Cluster")["Total_Withdrawals"].mean().sort_values()
levels = ["Very Low","Low","Medium-Low","Medium","Medium-High","High","Very High","Extreme"]
cluster_labels = {cid: levels[min(i, len(levels)-1)] + " Demand ATMs"
                  for i, (cid, _) in enumerate(cluster_means.items())}
df["Cluster_Label"] = df["Cluster"].map(cluster_labels)

print("\n── Cluster Summary ────────────────────────────────────────────────")
print(df.groupby("Cluster_Label")[["Total_Withdrawals","Total_Deposits","Nearby_Competitor_ATMs"]].mean().round(0))

# ── Visualize Clusters ───────────────────────────────────────────────────
palette = sns.color_palette("tab10", best_k)
plt.figure(figsize=(12, 6))
for cluster_id, label in cluster_labels.items():
    subset = df[df["Cluster"] == cluster_id]
    plt.scatter(subset["Total_Withdrawals"], subset["Total_Deposits"],
                label=label, alpha=0.4, s=30, color=palette[cluster_id])
plt.title("4.3 ATM Clusters: Withdrawals vs Deposits", fontsize=14, fontweight="bold")
plt.xlabel("Total Withdrawals"); plt.ylabel("Total Deposits")
plt.legend(loc="upper right", fontsize=8)
plt.tight_layout(); plt.savefig("4_3_clusters.png", dpi=100); plt.show()
print("📌 Observation: Clusters show distinct demand bands — high-demand ATMs should be prioritized for more frequent cash replenishment.")

df["Cluster_Label"].value_counts().plot(kind="bar", color="steelblue", edgecolor="white")
plt.title("4.4 Number of Records per Cluster", fontsize=14, fontweight="bold")
plt.xlabel("Cluster"); plt.ylabel("Count"); plt.xticks(rotation=30)
plt.tight_layout(); plt.savefig("4_4_cluster_counts.png", dpi=100); plt.show()


